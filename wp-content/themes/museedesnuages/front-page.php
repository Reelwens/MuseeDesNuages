            </p>
            <div class="point point-17"></div>
            <p class="paragraph paragraph-17">
                <span class="important">Un art participe, pour une science participative : la météorologie.</span> Parmi les sciences naturalistes, dont on s’accorde à dire qu’elles furent les précurseurs des sciences participatives, la météorologie n’aurait pas pu se développer sans la contribution d’un réseau d’observateurs. Sur le terrain du climat, l’art est plus que jamais attentif à la participation, à la co-construction et à toutes formes d’interactions.
            </p>
            <div class="point point-18"></div>
            <p class="paragraph paragraph-18">
                <span class="important">Des collections de rêves.</span> Nos collections sont accrochées à des cimaises de rêves.<br />En partageant un morceau de paysage en demandant à un artiste d’installer une œuvre sur un site que vous choisissez, c’est vous qui agrandissez nos réserves. Ainsi, la visite de ne se limite pas aux murs d’une galerie, ne se concentre pas en un seul endroit, mais se parcourt à l’échelle de territoires reliés par une même grille de lecture, celle qui suit le fil de l’eau et déchiffre les signes dans le ciel.
            </p>
            <div class="point point-19"></div>
            <p class="paragraph paragraph-19">
                <span class="important">Nuage d’objets, première collection matérielle.</span><br />À la suite de l’inscription « musée des nuages » sur le château d’eau de Poitiers-Bellejouanne, le musée a réuni une collection d’objets prêtés. Chaque objet a été numéroté, classé, estampillé puis exposé. À la fin de l’exposition, le nuage d’objets a été dispersé et les prêts restitués. En récupérant leur bien, les prêteurs sont devenus conservateurs d’un objet appartenant à un musée qui leur appartient.
            </p>
