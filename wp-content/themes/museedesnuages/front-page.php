            </p>
            <div class="point point-7"></div>
            <p class="paragraph paragraph-7">
                Être dans les nuages c’est rêver. Le musée des nuages rêve d’un art écologique auquel tout participe. Que chaque chose y trouve sa place : les œuvres de musées et les œuvres privées, les trésors de l’instant et les joyaux éternels ; que l’art entre dans la chaine alimentaire de toutes les espèces ; qu’il survole les barrières ; qu’il habite les rêves des enfants comme ceux des nations.
            </p>
            <div class="point point-8"></div>
            <p class="paragraph paragraph-8">
