                <span class="important">En 1988, depuis l’espace clos d’une salle d’exposition,</span> Sylvain Soussan regarde par la fenêtre ; il observe le monde extérieur et décide d’associer patrimoine culturel et patrimoine naturel.
            </p>
            <div class="point point-2"></div>
            <p class="paragraph paragraph-2">
                Regardant par la fenêtre d’un musée, il observe une nature progressivement contrainte et surveillée, tel un objet de collection. <span class="important">La beauté du monde lui apparait alors aussi fragile que celle des œuvres d’art.</span>
            </p>
            <div class="point point-3"></div>
            <p class="paragraph paragraph-3">
                Retournant vers la vitrine qu’il avait quittée des yeux, il constate que la valeur d’une collection est déterminée par la rareté des pièces qu’elle abrite, alors qu’on apprécie les biens naturels pour leur abondance. <span class="important">Les biens naturels ne sont pas pour autant moins précieux.</span>
            </p>
            <div class="point point-4"></div>
            <p class="paragraph paragraph-4">
                <span class="important">Ainsi sera conçu le musée des nuages qui nous invite à explorer notre environnement autant que notre sensibilité.</span> Partir dans les nuages, c’est aussi rêver et s’affranchir des cadres, c’est laisser s’emballer notre imagination ; le musée des nuages sera un musée sans mur et ouvert au partage.
            </p>
